    private void NoKartuTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NoKartuTextFieldActionPerformed
        String noKartu = NoKartuTextField.getText();
        if (!noKartu.matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Nomor kartu tidak valid!\nNomor kartu harus berupa angka.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
        }

        if (noKartu.length() <= 11 || noKartu.length() >= 13) {
            JOptionPane.showMessageDialog(null, "Nomor kartu tidak valid!\nNomor kartu harus memiliki 11-13 digit.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_NoKartuTextFieldActionPerformed

