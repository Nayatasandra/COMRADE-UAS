    private void bayarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarButtonActionPerformed
            String bank = bankTextField.getText();
            String noKartu = NoKartuTextField.getText();
            if(bank.equals("") && noKartu.equals("")){
                JOptionPane.showMessageDialog(null, "Bank dan No Kartu Tidak Boleh Kosong!", "Caution!", JOptionPane.WARNING_MESSAGE);
                
            }
            else if(bank.equals("")){
                JOptionPane.showMessageDialog(null, "Bank Tidak Boleh Kosong!", "Caution!", JOptionPane.WARNING_MESSAGE);
                
            }
            else if(noKartu.equals("")){
                JOptionPane.showMessageDialog(null, "No Kartu Tidak Boleh Kosong!", "Caution!", JOptionPane.WARNING_MESSAGE);
                
            }
            else {
                JOptionPane.showMessageDialog(null, "Pembayaran berhasil!");
            }
    }//GEN-LAST:event_bayarButtonActionPerformed

